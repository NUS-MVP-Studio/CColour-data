    /**
     * Get the closest color name from our list
     *
     * @param r
     * @param g
     * @param b
     * @return
     */
    fun getColorNameFromRgb(r: Int, g: Int, b: Int): String {
        var colorList = initColorList()
        colorList.addAll(initGreyColorList())
        var closestMatch: ColorName? = null
        var minMSE = Int.MAX_VALUE
        var mse: Int
        for (c in colorList) {
            mse = c.computeMSE(r, g, b)
            if (mse < minMSE) {
                minMSE = mse
                closestMatch = c
            }
        }
        return closestMatch?.name ?: "No matched color name."
    }

    /**
     * Convert hexColor to rgb, then call getColorNameFromRgb(r, g, b)
     *
     * @param hexColor
     * @return
     */
    fun getColorNameFromHex(hexColor: Int): String {
        val r = hexColor and 0xFF0000 shr 16
        val g = hexColor and 0xFF00 shr 8
        val b = hexColor and 0xFF
        return getColorNameFromRgb(r, g, b)
    }

    /**
     * Generate closest color name from our list
     *
     * @param h :Float 0-360
     * @param s :Float 0-1
     * @param l :Float 0-1
     * @return
     */
    fun getColorNameFromHsl(h: Float, s: Float, l:Float): String {
        if ((s < 0.11f) || (s < 0.13f && (l < 0.2f || l > 0.75f) ) || (s < 0.18f && ( l > 0.49f && l < 0.6f) ) // Grey
            || (l > 0.94f) // White
            || (l < 0.1f) || ((s < 0.11f) && (l < 0.21f))) // Black
        {
            val colorList = initGreyColorList()
            var colour: String
            if (l > 0.94f) {
                colour = "White"
            } else if ((l < 0.1f) || (s < 0.2f && l < 0.25f)){
                colour = "Black"
            } else {
                colour = "Grey"
            }
            for (c in colorList) {
                if (c.h == h && c.s == s && c.l == l)
                    if (c.name.contains(colour, ignoreCase = true) ) {
                        colour = c.name
                    } else {
                        colour = c.name + "," + colour
                    }
            }
            Log.d("getColorNameFromHsl", "h:$h, s:$s, l:$l, col:$colour")
            return colour
        } else {
            val colorList = initColorList()
            var matchList= colorList.filter { it ->
                if((h + 20f) > 360f){
                    (it.h > (h - 20f)) || (it.h < (h + 20f - 360f))
                } else if ((h - 20f) < 0f) {
                    (it.h < (h + 20f)) || it.h > (h - 20f + 360f)
                } else {
                    (it.h < (h + 20))  && (it.h > (h - 20))
                }
            }
            // Log.d("getColorNameFromHsl", "list size:"+ matchList.size.toString() +"(h-20f):" + (h-20f).toString() + "(h-20f+360f):" + (h - 20f + 360f).toString())
            var closestMatch: ColorName? = null
            var minMSE = Float.MAX_VALUE
            var mse: Float
            for (c in matchList) {
                mse = c.computeMseHSL(h,s,l)
                if (mse < minMSE) {
                    minMSE = mse
                    closestMatch = c
                }
            }
            Log.d("getColorNameFromHsl", "h:$h, s:$s, l:$l, col:" + closestMatch?.name.toString())

            var colour = getColorHue(h)
            if (closestMatch != null) {
                if ( closestMatch.name.contains(colour, ignoreCase = true) ) {
                    colour = closestMatch.name
                } else {
                    colour = closestMatch.name + "," + colour
                }
            }

            return colour
        }
    }

    fun getColorHue(h: Float): String {
        if (h < 30) {
            return "Red"
        } else if (h < 42) {
            return "Orange"
        } else if (h < 65) {
            return "Yellow"
        } else if (h < 160) {
            return "Green"
        } else if (h < 267) {
            return "Blue"
        } else if (h < 310) {
            return "Purple"
        } else {
            return "Red"
        }
    }

